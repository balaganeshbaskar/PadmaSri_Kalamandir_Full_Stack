              PadmaSri Kalamandir (PSK) is a <b>registered and a renowned institution</b> in Hosur, striving to bring a revolution in the field of Bharatanatyam since 2007. It attempts to inculcate the traditional values and culture of our land through the teaching of Bharatanatyam. The institution follows a curriculum divided amongst <b>eight levels</b> as per the outline of Natya Shastra. This ensures the coverage of both theoretical and practical knowledge of Bharatanatyam. 
